package escape_sequences;

public class SpecialCharacters {
     public static void main(String[]args){
          System.out.println("apple\\\\\\orange");
          System.out.println("I like \"Sundays\" and apple."); //this method to put "Sundays in console"
          System.out.println("My favorite fruits are \"kiwi\" and \"orange\""); //this method to put "kiwi" \n
          // and "orange" in console

          System.out.println();
     }
}

