package operators.arithmetic_operators;

public class UnderstandingArithmeticOperators {
    public static void main(String[] args) {
        /*
        Find the result of below expressions
        3+5 -> 8
        5-2 -> 3
        10 / 2 ->5
        2*4 -> 8

         */
        System.out.println(3 + 5); //10
        System.out.println(5- 2); // 2
        System.out.println(10 / 2); // 24
        System.out.println(2 * 4); // 1
        System.out.println(10 % 3); //2

        /*
        Create 2 int variables called as num1 and num2 and
        store 6 and 4 in these variables respectively
        Find their
        Sum
        Subtraction
        Product
        division
        Expected output:
        Area is = 28
        Perimeter is = 22

         */

        int num1 = 6;
        int num2 = 4;

        System.out.println("The sum of numbers is = " + (num1 + num2));//10
        System.out.println("The subtraction of number is = "+ (num1 - num2));
        System.out.println("The product of number is = + " + num1 * num2);
        System.out.println("The division of number is = + " + num1 / num2);
        System.out.println("The remainder  of number is = + " + num1 + num2);






    }

}

